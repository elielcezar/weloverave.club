// Função para buscar todos os posts
export const getAllPosts = () => {
  return allPosts
}

// Função para buscar post por ID
export const getPostById = (id) => {
  return allPosts.find(post => post.id === parseInt(id))
}

// Função para buscar post por slug
export const getPostBySlug = (slug) => {
  return allPosts.find(post => post.slug === slug)
}

// Função para buscar posts por categoria
export const getPostsByCategory = (category) => {
  return allPosts.filter(post => post.category.toLowerCase() === category.toLowerCase())
}

// Função para buscar posts relacionados
export const getRelatedPosts = (postId, limit = 3) => {
  const currentPost = getPostById(postId)
  if (!currentPost) return []
  
  return allPosts
    .filter(post => post.id !== postId && post.category === currentPost.category)
    .slice(0, limit)
}

